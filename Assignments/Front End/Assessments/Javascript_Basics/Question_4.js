const person = {
  firstname: "John",
  lastname: "Doe"
};

// 1. Dot notation
console.log("First Name (dot):", person.firstname);
console.log("Last Name (dot):", person.lastname);

// 2. Bracket notation
console.log("First Name (bracket):", person["firstname"]);
console.log("Last Name (bracket):", person["lastname"]);
